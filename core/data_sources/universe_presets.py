def get_universe_preset(name: str) -> list[str]:
    """Return a static preset list of six-digit A-share symbols."""
    normalized = (name or "mini").strip().lower()
    if normalized not in PRESET_UNIVERSES:
        normalized = "mini"
    return list(PRESET_UNIVERSES[normalized])


def to_ts_code(symbol: str) -> str:
    """Normalize six-digit symbols or project ts_code values to ts_code."""
    clean = str(symbol).strip()
    if "." in clean:
        return clean
    suffix = "SH" if clean.startswith(("6", "688")) else "SZ"
    return f"{clean}.{suffix}"


def to_akshare_symbol(symbol: str) -> str:
    """Normalize six-digit symbols or project ts_code values to AKShare raw symbols."""
    return str(symbol).strip().split(".")[0]
